

public class Operators {
    public static void main(String args[]) {
        int A=10;       //Binary Operators: Two operands
        int B=5;
        System.out.println("A+B = "+(A+B));
        System.out.println("A-B = "+(A-B));
        System.out.println("A*B = "+(A*B));
        System.out.println("A/B = "+(A/B));
        System.out.println("A%B = "+(A%B));
    }
}